def get_locale() -> str:
    """Return the active locale from session, defaulting to 'en'."""
    try:
        return session.get('lang', 'en')
    except RuntimeError:
        return 'en'


def get_t() -> dict:
    """Return the translation dict for the current locale."""
    return TRANSLATIONS.get(get_locale(), TRANSLATIONS['en'])
